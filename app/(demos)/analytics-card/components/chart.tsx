const chartConfig = {
  views: {
    label: "Page Views",
  },
  desktop: {
    label: "Desktop",
    color: "rgb(var(--foreground-rgb))",
  },
  mobile: {
    label: "Mobile",
    color: "rgb(var(--foreground-rgb))",
  },
} satisfies ChartConfig;

// in percentage
const DESKTOP_COEFICIENT = 24;
const MOBILE_COEFICIENT = -5;

const MotionCard = motion.create(Card);

export function Chart() {
  const [activeChart, setActiveChart] =
    React.useState<keyof typeof chartConfig>("desktop");

  const total = React.useMemo(
    () => ({
      desktop: chartData.reduce((acc, curr) => acc + curr.desktop, 0),
      mobile: chartData.reduce((acc, curr) => acc + curr.mobile, 0),
    }),
    []
  );

  return (
    <MotionCard
      initial={{ opacity: 0, filter: "blur(4px)" }}
      animate={{ opacity: 1, filter: "blur(0px)" }}
      exit={{ opacity: 0, filter: "blur(4px)" }}
      transition={{ ...DEFAULT_TRANSITION, delay: 0.25 }}
    >
      <CardHeader className="flex overflow-hidden border-b border-neutral-800 p-0">
        {["desktop", "mobile"].map((key) => {
          const chart = key as keyof typeof chartConfig;
          return (
            <button
              key={chart}
              data-active={activeChart === chart}
              className="relative z-30 flex flex-1 flex-col border-t border-neutral-800 justify-center gap-1 px-6 py-4 text-left data-[active=true]:bg-neutral-800/20 sm:border-r last:border-r-0 sm:border-t-0 sm:px-6 sm:py-4 transition-colors duration-200"
              onClick={() => setActiveChart(chart)}
            >
              <span className="text-xs text-neutral-400">
                {chartConfig[chart].label}
              </span>

              <div className="flex items-center gap-2">
                <span className="text-lg font-bold text-neutral-100 leading-none sm:text-3xl">
                  <AnimatedNumber
                    value={total[key as keyof typeof total].toLocaleString()}
                  />
                </span>

                <div
                  className={cn(
                    "rounded-full px-2 py-1",
                    key === "desktop" ? "bg-green-900" : "bg-red-900"
                  )}
                >
                  <p
                    className={cn(
                      "text-xs font-medium",
                      key === "desktop" ? "text-green-500" : "text-red-400"
                    )}
                  >
                    {key === "desktop" ? "+" : ""}
                    {key === "desktop" ? DESKTOP_COEFICIENT : MOBILE_COEFICIENT}
                    %
                  </p>
                </div>
              </div>
            </button>
          );
        })}
      </CardHeader>
      <CardContent className="pt-8 px-4">
        <ChartContainer
          config={chartConfig}
          className="aspect-auto h-[250px] w-full"
        >
          <BarChart
            accessibilityLayer
            data={chartData}
            margin={{
              left: 12,
              right: 12,
            }}
          >
            <CartesianGrid vertical={false} />
            <XAxis
              dataKey="date"
              tickLine={false}
              axisLine={false}
              tickMargin={0}
              minTickGap={12}
              tickFormatter={(value) => {
                const date = new Date(value);
                return date.toLocaleDateString("en-US", {
                  month: "short",
                  day: "numeric",
                });
              }}
            />
            <ChartTooltip
              content={
                <ChartTooltipContent
                  className="w-[150px]"
                  nameKey="views"
                  labelFormatter={(value) => {
                    return new Date(value).toLocaleDateString("en-US", {
                      month: "short",
                      day: "numeric",
                      year: "numeric",
                    });
                  }}
                />
              }
            />
            <Bar dataKey={activeChart} fill={`var(--color-${activeChart})`} />
          </BarChart>
        </ChartContainer>
      </CardContent>
    </MotionCard>
  );
}
